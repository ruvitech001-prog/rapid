  lastName: string;
  email: string;
  phone: string;
  specialty: string;
  contractorId: string;
  startDate: string;
  endDate: string;
  rate: string;
  rateType: 'hourly' | 'daily' | 'monthly';
  bankName: string;
  accountNumber: string;
  ifscCode: string;
  panNumber: string;
  gstNumber: string;
  address: string;
  city: string;
  state: string;
